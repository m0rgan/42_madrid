int	l_rows_32(int mat[4][4], int c, int data[16])
{
	if (data[c] == 3)
	{
		if (data[c + 4] == 2)
		{
			if (mat[c -8][1] == 2 || mat[c -8][3] == 3)
			{
				mat[c -8][0] = 1;
				mat[c -8][1] = 2;
				mat[c -8][3] = 3;
			}
			if ((mat[c -8][0] == 1 && mat[c -8][1] == 3) || mat[c -8][3] == 2)
			{
				mat[c -8][0] = 1;
				mat[c -8][1] = 3;
				mat[c -8][3] = 2;
			}
			if (mat[c -8][0] == 2 || mat[c -8][3] == 1)
			{
				mat[c -8][0] = 2;
				mat[c -8][1] = 3;
				mat[c -8][3] = 1;
			}
		}
	}
	return (0);
}

int	l_rows_31(int mat[4][4], int c, int data[16])
{
	if (data[c] == 3)
	{
		if (data[c + 4] == 1)
		{
			if (mat[c -8][0] == 1 || mat[c -8][2] == 2)
			{
				mat[c -8][0] = 1;
				mat[c -8][1] = 3;
				mat[c -8][2] = 2;
			}
			if (mat[c -8][1] == 1 || mat[c -8][2] == 3)
			{
				mat[c -8][0] = 2;
				mat[c -8][1] = 1;
				mat[c -8][2] = 3;
			}
			if ((mat[c -8][0] == 2 && mat[c -8][1] == 3) || mat[c -8][2] == 1)
			{
				mat[c -8][0] = 2;
				mat[c -8][1] = 3;
				mat[c -8][2] = 1;
			}
		}
	}
	return (0);
}
