#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*list;
	t_list	*node;
	void	*cont;

	if (!lst || !f || !del)
		return (NULL);
	list = NULL;
	while (lst)
	{
		cont = f(lst->content);
		node = ft_lstnew(cont);
		if (!node)
		{
			del(cont);
			ft_lstclear(&list, (*del));
			return (list);
		}
		ft_lstadd_back(&list, node);
		lst = lst->next;
	}
	return (list);
}
